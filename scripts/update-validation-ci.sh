#!/bin/bash
# ============================================================================
# Name:        update-validation-ci.sh
# Version:     2.0.0
# Description: Sync validate-marketplace.sh + CI workflow to target marketplaces
# Source:      claude-code-in-avinyc/scripts/update-validation-ci.sh
# Usage:       ./scripts/update-validation-ci.sh
# Requires:    bash 4+
# Updated:     2026-03-13
# ============================================================================

set -e

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
ROOT_DIR="$(dirname "$SCRIPT_DIR")"

RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[0;33m'
CYAN='\033[0;36m'
BOLD='\033[1m'
NC='\033[0m'
CHECK_MARK="\xe2\x9c\x93"
CROSS_MARK="\xe2\x9c\x97"

TARGETS=(
    "/Users/avi/Development/code/AI/avi-ai"
    "/Users/avi/Development/code/Innovent/Agents/innovent-ai"
    "/Users/avi/Development/code/Innovent/Agents/innovent-ai-devops"
    "/Users/avi/Development/code/Innovent/Agents/innovent-rails-skills"
)

SOURCE_SCRIPT="$SCRIPT_DIR/validate-marketplace.sh"

# Read source version
SOURCE_VERSION=$(grep '# Version:' "$SOURCE_SCRIPT" | head -1 | sed 's/.*Version:\s*//' | tr -d ' ')
if [ -z "$SOURCE_VERSION" ]; then
    echo -e "${RED}Error: Cannot read version from validate-marketplace.sh${NC}"
    exit 1
fi

# Semver comparison using sort -V: returns 0 if $1 > $2
version_gt() {
    [ "$1" != "$2" ] && [ "$(printf '%s\n%s' "$1" "$2" | sort -V | head -1)" = "$2" ]
}

# Generate a thin CI workflow that calls the copied script
generate_workflow() {
    local source_version="$1"
    cat << 'WORKFLOW_EOF'
# ============================================================================
# Marketplace Validation CI
# Auto-generated by update-validation-ci.sh
WORKFLOW_EOF
    echo "# Source-Version: $source_version"
    cat << 'WORKFLOW_EOF'
# Do not edit manually — re-run update-validation-ci.sh to update
# ============================================================================

name: Validate Marketplace

on:
  push:
    branches: [main]
  pull_request:
    branches: [main]

permissions:
  contents: read
  pull-requests: write

jobs:
  validate:
    runs-on: ubuntu-latest
    steps:
      - name: Checkout
        uses: actions/checkout@v4

      - name: Setup Node.js
        uses: actions/setup-node@v4
        with:
          node-version: '20'

      - name: Validate Marketplace
        run: ./scripts/validate-marketplace.sh --ci

      - name: Comment on PR (on failure)
        if: failure() && github.event_name == 'pull_request'
        uses: actions/github-script@v7
        with:
          script: |
            github.rest.issues.createComment({
              issue_number: context.issue.number,
              owner: context.repo.owner,
              repo: context.repo.repo,
              body: '❌ **Marketplace validation failed.** Please run `./scripts/validate-marketplace.sh` locally to see details and fix issues before merging.'
            });
WORKFLOW_EOF
}

# Copy validate-marketplace.sh to target, replacing symlink if present
copy_script() {
    local target_scripts="$1"
    local target_script="$target_scripts/validate-marketplace.sh"

    mkdir -p "$target_scripts"

    # Remove symlink if it exists (replace with real file)
    if [ -L "$target_script" ]; then
        rm "$target_script"
        echo -e "  ${CYAN}i Replaced symlink with real file${NC}"
    fi

    cp "$SOURCE_SCRIPT" "$target_script"
    chmod +x "$target_script"
}

# --- Header ---
echo ""
echo -e "${CYAN}$(printf '\xe2\x95\x94')$(printf '\xe2\x95\x90%.0s' {1..50})$(printf '\xe2\x95\x97')${NC}"
echo -e "${CYAN}$(printf '\xe2\x95\x91')  CI Validation Sync                               $(printf '\xe2\x95\x91')${NC}"
echo -e "${CYAN}$(printf '\xe2\x95\x9a')$(printf '\xe2\x95\x90%.0s' {1..50})$(printf '\xe2\x95\x9d')${NC}"
echo ""
echo -e "Source: validate-marketplace.sh ${BOLD}v${SOURCE_VERSION}${NC}"
echo ""

TOTAL=${#TARGETS[@]}
SYNCED=0
IDX=0

for target in "${TARGETS[@]}"; do
    IDX=$((IDX+1))
    target_name=$(basename "$target")
    echo -e "${BOLD}[$IDX/$TOTAL] $target_name${NC}"

    if [ ! -d "$target" ]; then
        echo -e "  ${YELLOW}! Target directory not found: $target${NC}"
        echo ""
        continue
    fi

    target_scripts="$target/scripts"
    target_script="$target_scripts/validate-marketplace.sh"
    workflow_dir="$target/.github/workflows"
    workflow_file="$workflow_dir/validate.yml"

    # Check current version from the target's validate-marketplace.sh (not workflow)
    current_version=""
    if [ -f "$target_script" ] && [ ! -L "$target_script" ]; then
        current_version=$(grep '# Version:' "$target_script" | head -1 | sed 's/.*Version:\s*//' | tr -d ' ')
    fi

    display_current="${current_version:-"(none)"}"

    # Determine if update needed
    needs_update=false
    if [ -z "$current_version" ]; then
        needs_update=true
    elif version_gt "$SOURCE_VERSION" "$current_version"; then
        needs_update=true
    fi

    if [ "$needs_update" = true ]; then
        if [ -z "$current_version" ]; then
            echo -e "  Current: ${YELLOW}(none)${NC} -> Installing ${GREEN}v${SOURCE_VERSION}${NC}"
        else
            echo -e "  Current: ${YELLOW}v${current_version}${NC} -> Updating to ${GREEN}v${SOURCE_VERSION}${NC}"
        fi

        # 1. Copy the script (real file, not symlink)
        copy_script "$target_scripts"
        echo -e "  ${GREEN}${CHECK_MARK} scripts/validate-marketplace.sh copied${NC}"

        # 2. Generate thin workflow
        mkdir -p "$workflow_dir"
        generate_workflow "$SOURCE_VERSION" > "$workflow_file"
        echo -e "  ${GREEN}${CHECK_MARK} .github/workflows/validate.yml generated${NC}"

        SYNCED=$((SYNCED+1))
    else
        echo -e "  Current: ${GREEN}v${current_version}${NC} (up to date)"
        echo -e "  ${GREEN}${CHECK_MARK} No update needed${NC}"
        SYNCED=$((SYNCED+1))
    fi
    echo ""
done

# --- Footer ---
echo -e "$(printf '\xe2\x95\x90%.0s' {1..52})"
echo -e "  ${GREEN}${BOLD}RESULT: ${SYNCED}/${TOTAL} marketplaces synced ${CHECK_MARK}${NC}"
echo -e "$(printf '\xe2\x95\x90%.0s' {1..52})"
echo ""
